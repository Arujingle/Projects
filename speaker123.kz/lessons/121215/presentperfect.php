 <p> Правила применения Презент перфект (Present Perfect)</br>
  
  Презент перфект (Present Perfect), связь прошлого с настоящим, наглядно можно изобразить такой схемой
  
  <img src="Prezent-perfekt-krugi.png">
  
  Строится эта конструкция достаточно просто:

Вспомогательный глагол have/has + Past Participle. Вместо полной формы have/has очень часто англичане используют сокращенный (contracted) вариант ‘ve/’s — I’ve been there, She’s been there.</p>

<p>Обратите особое внимание на сокращения she has = she’s, he has = he’s — тут важно не спутать  с другой формой — she is, he is, которая в сокращенном варианте точно также выглядит и произносится. Различить можно по применению Past Participle после she has, he has, а также из контекста.



